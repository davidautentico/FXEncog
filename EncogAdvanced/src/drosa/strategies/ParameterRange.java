package drosa.strategies;

public class ParameterRange {

	int paramOrder;
	double 	lowRangeD;
	int 	lowRangeI;
	double 	highRangeD;
	int 	highRangeI;
	
	public int getParamOrder() {
		return paramOrder;
	}
	public void setParamOrder(int paramOrder) {
		this.paramOrder = paramOrder;
	}
	public double getLowRangeD() {
		return lowRangeD;
	}
	public void setLowRangeD(double lowRangeD) {
		this.lowRangeD = lowRangeD;
	}
	public int getLowRangeI() {
		return lowRangeI;
	}
	public void setLowRangeI(int lowRangeI) {
		this.lowRangeI = lowRangeI;
	}
	public double getHighRangeD() {
		return highRangeD;
	}
	public void setHighRangeD(double highRangeD) {
		this.highRangeD = highRangeD;
	}
	public int getHighRangeI() {
		return highRangeI;
	}
	public void setHighRangeI(int highRangeI) {
		this.highRangeI = highRangeI;
	}
	
	
	
}
